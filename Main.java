    System.out.println(x*y); // this will output 40
    System.out.println(x/y); // this will output 1 because there's no decimals in integers
    System.out.println(x%y); // this will output 3
    System.out.print("\n");

    int sum;
    sum = x+y; // 8+5 = 13 so z = 13
